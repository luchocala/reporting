const STATUS_NAME_HINTS = ["estado", "status", "state", "approved", "activo"];
const CLIENT_NAME_HINTS = ["cliente", "client", "customer", "razon", "nombre", "name", "empresa", "account"];
const ID_NAME_HINTS = ["id", "uuid", "key"];

function titleize(value) {
  return String(value || "")
    .replace(/_/g, " ")
    .replace(/([a-z])([A-Z])/g, "$1 $2")
    .replace(/\s+/g, " ")
    .trim()
    .replace(/^./, (letter) => letter.toUpperCase());
}

function normalizeKey(value) {
  return String(value || "").toLowerCase();
}

function hasAnyHint(columnKey, hints) {
  const normalized = normalizeKey(columnKey);
  return hints.some((hint) => normalized.includes(hint));
}

function isEmpty(value) {
  return value === null || value === undefined || value === "";
}

function getUniqueValues(rows, key) {
  return Array.from(
    new Set(
      (rows || [])
        .map((row) => row?.[key])
        .filter((value) => !isEmpty(value))
        .map(String)
    )
  );
}

/**
 * Mantengo esta función por compatibilidad con otros imports,
 * pero ya no detecta tipos visuales automáticamente.
 *
 * La regla nueva es:
 * - Por defecto: text
 * - Excepciones: se definen manualmente por página con columnOverrides,
 *   transforms, lookups o virtualColumns.
 */
export function detectColumnType() {
  return "text";
}

export function detectPrimaryKey(rows = [], configuredPrimaryKey) {
  if (configuredPrimaryKey) return configuredPrimaryKey;

  const firstRow = rows[0] || {};
  const keys = Object.keys(firstRow);

  if (keys.includes("id")) return "id";

  const exactId = keys.find((key) => normalizeKey(key).endsWith("id"));
  if (exactId) return exactId;

  return keys.find((key) => hasAnyHint(key, ID_NAME_HINTS)) || null;
}

function normalizeVirtualColumn(column) {
  return {
    type: "text",
    primary: false,
    locked: false,
    autoGenerated: false,
    ...column,
    label: column.label || titleize(column.key),
    virtual: true,
  };
}

export function inferColumns(rows = [], options = {}) {
  const firstRow = rows[0] || {};
  const primaryKey = detectPrimaryKey(rows, options.primaryKey);

  const hiddenColumns = new Set(options.hiddenColumns || []);
  const columnLabels = options.columnLabels || {};
  const overrides = options.columnOverrides || {};
  const transforms = options.transforms || {};
  const lookups = options.lookups || {};
  const enumOptions = options.enumOptions || {};
  const virtualColumns = Array.isArray(options.virtualColumns) ? options.virtualColumns : [];
  const virtualColumnKeys = new Set(
  virtualColumns
    .map((column) => column?.key)
    .filter(Boolean)
);

  const baseKeys =
    Array.isArray(options.columnOrder) && options.columnOrder.length > 0
      ? options.columnOrder
      : Object.keys(firstRow);

  const keys = baseKeys.filter(
  (key) => !hiddenColumns.has(key) && !virtualColumnKeys.has(key)
);

  const columns = keys.map((key) => {
    const override = overrides[key] || {};
    const transform = transforms[key];
    const lookup = lookups[key];
    const uniqueValues = enumOptions[key] || getUniqueValues(rows, key);

    const label =
      transform?.label ||
      lookup?.label ||
      override.label ||
      columnLabels[key] ||
      titleize(key);

    const render =
      transform?.render ||
      override.render;

    const type =
      override.type ||
      transform?.type ||
      lookup?.type ||
      "text";

    return {
  key,
  ...override,
  label,
  type,
  primary: override.primary ?? hasAnyHint(key, CLIENT_NAME_HINTS),
  locked: override.locked ?? key === primaryKey,
  autoGenerated: override.autoGenerated ?? key === primaryKey,
  formOptions: override.formOptions || enumOptions[key],
  bulkOptions: override.bulkOptions || enumOptions[key] || uniqueValues,
  lookup,
  transform,
  render,
};
  });

  const normalizedVirtualColumns = virtualColumns
    .filter((column) => column?.key && !hiddenColumns.has(column.key))
    .map(normalizeVirtualColumn);

  const allColumns = [...columns, ...normalizedVirtualColumns];

  if (!allColumns.some((column) => column.primary)) {
    const firstTextColumn = allColumns.find(
      (column) => column.type === "text" && column.key !== primaryKey
    );

    if (firstTextColumn) {
      firstTextColumn.primary = true;
    }
  }

  return allColumns;
}

export function inferPrimaryFilters(columns = [], rows = [], options = {}) {
  if (Array.isArray(options.filters)) return options.filters;

  const statusColumn = columns.find(
    (column) =>
      column.type === "status" ||
      column.type === "badge" ||
      hasAnyHint(column.key, STATUS_NAME_HINTS)
  );

  const clientColumn = columns.find((column) => hasAnyHint(column.key, CLIENT_NAME_HINTS));

  const filters = [];

  /**
   * Ya no inferimos filtros de fecha automáticamente porque dejamos de inferir
   * tipos visuales. Si una página necesita filtro de fecha, conviene pasarlo
   * manualmente con config.filters.
   */

  if (statusColumn) {
    filters.push({ key: statusColumn.key, label: statusColumn.label, type: "select" });
  }

  if (clientColumn && clientColumn.key !== statusColumn?.key) {
    filters.push({ key: clientColumn.key, label: clientColumn.label, type: "select" });
  }

  if (filters.length < 3) {
    columns
      .filter((column) => !filters.some((filter) => filter.key === column.key || filter.sourceKey === column.key))
      .filter((column) => ["badge", "status", "text"].includes(column.type))
      .filter((column) => getUniqueValues(rows, column.key).length > 0 && getUniqueValues(rows, column.key).length <= 12)
      .slice(0, 3 - filters.length)
      .forEach((column) => filters.push({ key: column.key, label: column.label, type: "select" }));
  }

  return filters.slice(0, 3);
}

export function buildBadgeStyles(columns = [], rows = [], options = {}) {
  const configured = options.badgeStyles || {};
  const enumOptions = options.enumOptions || {};
  const colorCycle = ["blue", "green", "yellow", "red", "slate"];

  return columns.reduce((acc, column) => {
    if (!["status", "badge"].includes(column.type)) return acc;

    if (configured[column.key]) {
      acc[column.key] = configured[column.key];
      return acc;
    }

    const values = enumOptions[column.key] || getUniqueValues(rows, column.key);

    acc[column.key] = Object.fromEntries(
      values.map((value, index) => [String(value), colorCycle[index % colorCycle.length]])
    );

    return acc;
  }, {});
}

export function buildEntitySectionFromRows(config = {}, rows = []) {
  const effectiveRows = Array.isArray(rows) ? rows : [];

  const columns =
    Array.isArray(config.columns) && config.columns.length > 0
      ? config.columns
      : inferColumns(effectiveRows, config);

  const primaryFilters =
    Array.isArray(config.primaryFilters) && config.primaryFilters.length > 0
      ? config.primaryFilters
      : inferPrimaryFilters(columns, effectiveRows, config);

  return {
    ...config,
    key: config.key,
    group: config.group,
    title: config.title,
    subtitle: config.subtitle,
    path: config.path,
    createPath: config.createPath,
    endpoint: config.endpoint,
    tableName: config.tableName,
    dataSource: config.dataSource,
    actionsKey: config.actionsKey,
    statsKey: config.statsKey,
    emptyMessage: config.emptyMessage,
    columns,
    rows: effectiveRows,
    primaryFilters,
    laneField:
      config.laneField ||
      columns.find((column) => column.type === "status")?.key ||
      "estado",
    badgeStyles: {
      ...buildBadgeStyles(columns, effectiveRows, config),
      ...(config.badgeStyles || {}),
    },
    actions: config.actions || {},
    headerActions: config.headerActions || [],
    statsCards: config.statsCards || [],
    rowActions: config.rowActions || [],
    onRowAction: config.onRowAction,
    onRefresh: config.onRefresh,
    loading: config.loading || false,
    error: config.error || "",
    lookupMaps: config.lookupMaps || {},
    defaultVisibleColumns:
      Array.isArray(config.defaultVisibleColumns) && config.defaultVisibleColumns.length > 0
        ? config.defaultVisibleColumns
        : columns.map((column) => column.key),
  };
}